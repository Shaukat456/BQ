# Example 1: Using the int() Function

# Convert a string to an integer

string = "123"
number = int(string)
print(number, type(number))

# Convert a float to an integer

float_number = 3.14
integer_number = int(float_number)
print(integer_number, type(integer_number))

# Convert a boolean to an integer

boolean = True
integer_boolean = int(boolean)
print(integer_boolean, type(integer_boolean))

# Convert a list to an integer

numbers = [1, 2, 3]
integer_list = int(numbers)

# Example 2: Using the tuple() Function

# Convert a list to a tuple

numbers = [1, 2, 3]
number_tuple = tuple(numbers)
print(number_tuple, type(number_tuple))

# Convert a string to a tuple

myname = "John"
name_tuple = tuple(myname)
print(name_tuple)


